// Ejemplo simple de variables en JavaScript

// String = cadenas de texto
var datoNombre = "Aurelio"
var datoApellido = "Lombardi"
var datoTelefono = "1133223311"

// Number = números (enteros o decimales)
var datoEdad = 25

// Boolean = verdadero o falso
var datoInscripto = true


// Mostrar los valores en consola

console.log("Nombre:", datoNombre)
console.log("Apellido:", datoApellido)
console.log("Telefono:", datoTelefono)
console.log("Edad:", datoEdad)
console.log("Inscripto:", datoInscripto)


// También podemos usar las variables juntas

console.log("El alumno", datoNombre, datoApellido, "tiene", datoEdad, "años")